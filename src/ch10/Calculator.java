package ch10;

public class Calculator {
	

	public int plus(int x, int y) {
		int result = x+y;
		return result;
	}
	
	public int multiple(int x, int y) {
		int result1 = x*y;
		return result1;
	}
	
	public int minus(int x, int y) {
		int result2 = x-y;
		return result2;
	}
	
	public int divide(int x, int y) {
		int result = x/y;
		return result;
	}

}
